export async function addCapture({ code, bsc_output, files, file = null, source = 'bsc', session_id = null, cause = null }) {
    const db = await ensureDB();
    const timestamp = new Date().toISOString();
    const sid = session_id || _currentSessionId;
    const result = upsertCapture(db, { code, timestamp, bsc_output, files, file, source, session_id: sid, cause, status: 'unresolved' });
    await saveDB();
    return result;
}

/**
 * Batch insert captures — opens DB once, inserts all, saves once.
 * P2-1 fix: replaces O(codes×files) individual addCapture calls.
 */
export async function addCapturesBatch(entries) {
    if (!entries || entries.length === 0) return [];
    const db = await ensureDB();
    const timestamp = new Date().toISOString();
    const sid = _currentSessionId;
    const results = [];
    for (const entry of entries) {
        const result = upsertCapture(db, {
            code: entry.code,
            timestamp,
            bsc_output: entry.bsc_output || '',
            files: entry.files || null,
            file: entry.file || null,
            source: entry.source || 'bsc',
            session_id: entry.session_id || sid,
            cause: entry.cause || null,
            status: 'unresolved',
        });
        results.push(result);
    }
    await saveDB();
    return results;
}

