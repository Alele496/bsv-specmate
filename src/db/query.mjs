export async function saveWarningSnapshot(snapshot_id, warnings) {
    const db = await ensureDB();
    const timestamp = new Date().toISOString();
    for (const w of warnings) {
        insertWarning(db, {
            snapshot_id,
            timestamp,
            file: w.file,
            line: w.line || null,
            code: w.code,
            message: w.message,
        });
    }
    await saveDB();
}

